
function clickMenu() {
    if (menu.style.display == `block`) {
        menu.style.display = `none`
    } else { menu.style.display = `block` }
}

function resize() {
    if (window.innerWidth >= 768) {
        menu.style.display = `none`
    } else { menu.style.display = `block` }
}


